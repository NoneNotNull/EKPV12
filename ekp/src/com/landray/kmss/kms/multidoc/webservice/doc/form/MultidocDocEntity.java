package com.landray.kmss.kms.multidoc.webservice.doc.form;

public class MultidocDocEntity {
	private String fdTemplateName;
	private String docSubject;
	private String docStatus;
	private String docPublishTime;
	private String docAutherName;
	private String docUrl;

	public String getFdTemplateName() {
		return fdTemplateName;
	}

	public void setFdTemplateName(String fdTemplateName) {
		this.fdTemplateName = fdTemplateName;
	}

	public String getDocSubject() {
		return docSubject;
	}

	public void setDocSubject(String docSubject) {
		this.docSubject = docSubject;
	}

	public String getDocStatus() {
		return docStatus;
	}

	public void setDocStatus(String docStatus) {
		this.docStatus = docStatus;
	}

	public String getDocPublishTime() {
		return docPublishTime;
	}

	public void setDocPublishTime(String docPublishTime) {
		this.docPublishTime = docPublishTime;
	}

	public String getDocAutherName() {
		return docAutherName;
	}

	public void setDocAutherName(String docAutherName) {
		this.docAutherName = docAutherName;
	}

	public String getDocUrl() {
		return docUrl;
	}

	public void setDocUrl(String docUrl) {
		this.docUrl = docUrl;
	}
}
